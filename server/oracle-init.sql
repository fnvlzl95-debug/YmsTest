-- ============================================
-- 1. 테이블 생성
-- ============================================

-- ■ 직원 마스터
CREATE TABLE MST_EMPLOYEE (
    "Id"                NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "UserId"            VARCHAR2(30)  NOT NULL,
    "EmpNo"             VARCHAR2(20)  NOT NULL,
    "HName"             VARCHAR2(50)  NOT NULL,
    "EName"             VARCHAR2(50)  NOT NULL,
    "DeptCode"          VARCHAR2(20)  NOT NULL,
    "DeptName"          VARCHAR2(50)  NOT NULL,
    "SingleId"          VARCHAR2(100) NOT NULL,
    "SingleMailAddr"    VARCHAR2(150) NOT NULL,
    "Site"              VARCHAR2(20)  NOT NULL,
    CONSTRAINT UQ_EMPLOYEE_USERID UNIQUE ("UserId"),
    CONSTRAINT UQ_EMPLOYEE_EMPNO  UNIQUE ("EmpNo")
);

-- ■ 설비 마스터
CREATE TABLE DDB_EQUIPMENT_MST (
    "Id"            NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "LineId"        VARCHAR2(20)  NOT NULL,
    "LargeClass"    VARCHAR2(20)  NOT NULL,
    "EqpType"       VARCHAR2(50)  NOT NULL,
    "EqpId"         VARCHAR2(30)  NOT NULL,
    "EqpGroupName"  VARCHAR2(50)  NOT NULL,
    CONSTRAINT UQ_EQUIPMENT_EQPID UNIQUE ("EqpId")
);

-- ■ 설비 권한
CREATE TABLE DDB_OPENLAB_AUTH (
    "Id"        NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "Site"      VARCHAR2(20)  NOT NULL,
    "EqpName"   VARCHAR2(30)  NOT NULL,
    "AuthType"  VARCHAR2(20)  NOT NULL,
    "EmpNo"     VARCHAR2(20)  NOT NULL,
    CONSTRAINT FK_AUTH_EMPLOYEE FOREIGN KEY ("EmpNo") REFERENCES MST_EMPLOYEE("EmpNo"),
    CONSTRAINT UQ_AUTH UNIQUE ("Site", "EqpName", "AuthType", "EmpNo")
);

-- ■ 설비 예약
CREATE TABLE DDB_EQUIPMENT_RESV (
    "Id"            NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "IssueNo"       VARCHAR2(50)  NOT NULL,
    "EquipmentId"   NUMBER(10)    NOT NULL,
    "EqpId"         VARCHAR2(30)  NOT NULL,
    "LineId"        VARCHAR2(20)  NOT NULL,
    "LargeClass"    VARCHAR2(20)  NOT NULL,
    "EmpName"       VARCHAR2(30)  NOT NULL,
    "EmpNum"        VARCHAR2(20)  NOT NULL,
    "ReservedDate"  TIMESTAMP     NOT NULL,
    "Purpose"       VARCHAR2(200) NOT NULL,
    "Status"        VARCHAR2(20)  NOT NULL,
    "CreatedAt"     TIMESTAMP     DEFAULT SYSTIMESTAMP,
    CONSTRAINT FK_RESV_EQUIPMENT FOREIGN KEY ("EquipmentId") REFERENCES DDB_EQUIPMENT_MST("Id"),
    CONSTRAINT UQ_RESV_ISSUENO UNIQUE ("IssueNo")
);

CREATE INDEX IDX_RESV_LINE_CLASS_DATE ON DDB_EQUIPMENT_RESV ("LineId", "LargeClass", "ReservedDate");

-- ■ 승인 알림
CREATE TABLE DDB_APPROVAL_NOTI (
    "Id"                    NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "IssueNo"               VARCHAR2(50)  NOT NULL,
    "ApprovalSeq"           VARCHAR2(10)  NOT NULL,
    "ApprovalReq"           VARCHAR2(10)  NOT NULL,
    "NotiUserId"            VARCHAR2(30)  NOT NULL,
    "NotiUserName"          VARCHAR2(50)  NOT NULL,
    "NotiUserDeptCode"      VARCHAR2(20)  NOT NULL,
    "NotiUserDeptName"      VARCHAR2(50)  NOT NULL,
    "NotiSingleMailAddr"    VARCHAR2(150) NOT NULL,
    "LastUpdateTime"        TIMESTAMP     DEFAULT SYSTIMESTAMP,
    CONSTRAINT FK_NOTI_EMPLOYEE FOREIGN KEY ("NotiUserId") REFERENCES MST_EMPLOYEE("UserId"),
    CONSTRAINT UQ_NOTI UNIQUE ("IssueNo", "ApprovalSeq", "NotiUserId")
);

-- ■ UI 검색 이력
CREATE TABLE TSP_YMS_UI_SEARCH_HISTORY (
    "Id"            NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "AppId"         VARCHAR2(100)  NOT NULL,
    "ControlId"     VARCHAR2(100)  NOT NULL,
    "UserId"        VARCHAR2(30)   NOT NULL,
    "SearchTime"    TIMESTAMP      DEFAULT SYSTIMESTAMP,
    "SearchValue"   VARCHAR2(1000) NOT NULL
);

CREATE INDEX IDX_SEARCH_HISTORY ON TSP_YMS_UI_SEARCH_HISTORY ("AppId", "ControlId", "UserId", "SearchTime");


